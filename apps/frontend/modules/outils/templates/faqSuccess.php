    <h3>1. Comment m'inscrire ?</h3>
    <div>
        <p>Vous pouvez créer votre compte en cliquant simplement sur le bouton «Je m’inscris ».</p>
        <p>L'inscription est simple et se passe en <strong>3 étapes</strong> :</p>
        <ul>
            <li>Lisez et acceptez la charte de bonne conduite.</li>
            <li>Saisissez votre adresse email et un mot de passe. Ils serviront d'identifiants de compte. Le mail sera par la suite utilisé pour vos contacts avec les autres utilisateurs du site.</li>
            <li>Remplissez le formulaire de renseignement. Les données n'apparaitront pas telles quelles sur le site pour préserver votre anonymat.</li>
        </ul>
        <p>Une fois que vous aurez cliqué sur « Valider », vous recevrez un mail de confirmation d'inscription vous rappelantvos identifiant et mot de passe. Votre compte créé, vous pouvez déposer vos trajets et entrer en contact avec d'autres covoitureurs.</p>
        <p>Si vous ne disposez pas d’adresse mail, vous pouvez également vous inscrire. Covoiturage+ sera votre
            interlocuteur. Néanmoins, vous ne pourrez pas vous connecter sur le site internet pour modifier votre compte ou déposer vos trajets.</p>
    </div>
    <h3>2. Comment faire une recherche de trajet ?</h3>
    <div>
        <p>La recherche de trajet par critères s'effectue simplement :</p>
        <ul>
            <li>entrer votre ville de départ : sélectionner la ville proposée par l'aide intuitive.</li>
            <li>saisissez votre destination : valider la proposition intuitive adéquate.</li>
            <li>cliquer sur « OK »</li>
        </ul>
        <p>En cliquant sur : >> Recherche avancée ?, vous accédez à un module de recherche plus détaillé. Vous pouvez alors préciser des critères de recherche plus fins : par exemple sur le statut du covoitureur recherché (vous souhaitez un conducteur ou un passager), un trajet rattaché à une entreprise (covoiturage entre collaborateurs, ou intérimaires non motorisés…), à un événement spécifique (sélectionnez-le dans la liste déroulante), ainsi que le type de trajets que vous recherchez : régulier ou occasionnel.</p>
        <p>La liste des trajets répondant à votre recherche s'affichera automatiquement dans une page de résultats.<br/>
            Cliquez sur la ligne, vous ferez apparaître les principales informations. Vous pouvez ensuite afficher la fiche détaillée, mais également contacter directement le covoitureur.</p>   
    </div>
    <h3>3. Comment proposer un trajet ?</h3>
    <div>
        <p>Pour mettre en ligne une proposition de trajet, vous devez être inscrit et identifié sur le site. Identifiez-vous par le module de connexion à droite, ou inscrivez-vous en passant par « Je m’inscris». Déposez ensuite votre trajet .
            Dans votre module de gestion de compte, à droite, cliquez sur Mes Trajets : pour créer votre parcours, clisuez sur Nouveau.</p>
        <p>Le dépôt de trajet se fait en 3 étapes :</p>
        <ul>
            <li>Indiquez d’abord vos lieux de départ et de destination</li>
            <li>Sélectionnez ensuite vos horaires</li>
            <li>Complétez ensuite ces informations avec des précisions sur votre trajet.</li>
        </ul>
        <p>Une fois validé, votre offre sera accessible à la consultation. Les personnes intéressées vous contacteront
            directement par e-mail.</p>

    </div>
    <h3>4. Comment modifier ou supprimer son trajet ?</h3>
    <div>
        <p>Connectez-vous à votre compte, avec votre adresse mail et votre mot de passe.</p>
        <p>Cliquez ensuite sur « Mes Trajets » .</p>
        <p>Tous vos trajets sont affichés. Pour chacun, vous pouvez le visualiser, le modifier, le supprimer ou le désactiver temporairement (pour les vacances par exemple).</p>
        <p>Si vous choisissez de le modifier, vous accédez au formulaire de dépôt de trajet pré-renseigné. Vous pouvez
            alors modifier vos critères, avant de valider votre saisie.</p>
        <p>Si vous choisissez de le supprimer, il vous sera demandé une confirmation d’action.</p>
    </div>
    <h3>5. Comment contacter un covoitureur ?</h3>
    <div>
        <p>Afin de conserver l’anonymat et de préserver les données personnelles des covoitureurs, aucune adresse (mail
            ou postale) ne figure sur nos fiches. Si vous êtes intéressé par un trajet, et que le covoitureur n’a pas souhaité afficher son numéro de téléphone, nous vous proposons de lui envoyer un message par le biais du site.</p>
        <p>Vous disposez pour cela d’un module dans la fiche trajet :</p>
        <p>Saisissez votre message avant de l’envoyer. C’est le covoitureur qui vous répondra directement.</p>                    
    </div>
    <h3>6. Comment calculer les frais ?</h3>
    <div>
        <p>En général, les frais sont partagés entre les covoitureurs. Certains calculent et partagent simplement les frais d’essence (et de péage le cas échéant), d’autres prennent aussi en compte l’usure de la voiture et l’assurance (auquel cas, vous devez calculer le nombre de kilomètres parcourus).</p>
        <p>Pour vous aider, le site propose un éco-calculateur, disponible sur une page spécifique, mais également un
            module de calcul automatique de frais, disponible sur la fiche trajet directement, lors du dépôt. Il vous suffit de
            cliquer sur Calculer après avoir indiqué votre itinéraire ainsi que votre souhait de partager les frais. Ce calcul est
            basé sur l’éco-calculette de l’Ademe. </p>
        <p>Dans certains cas et pour des trajets réguliers, les covoitureurs choisissent généralement d’alterner leurs véhicules. Dans ce cas, il peut n’y avoir aucun partage de frais en numéraire.</p>
        <p>Attention : la participation demandée ne doit pas excéder le montant des frais réellement subis par le conducteur.</p>
    </div>

    <h3>7. A quoi dois-je penser avant de partir ?</h3>
    <div>
        <p>Avant le départ, convenez entre covoitureurs des modalités du voyage, notamment :</p>
        <ul>
            <li>lieu et horaire de rendez-vous et de dépose précis,</li>
            <li>temps d’attente maximal des passagers,</li>
            <li>échange de coordonnées (pour prévenir d’un éventuel désistement ou d’un retard),</li>
            <li>partage des frais, modalités et délai de paiement,</li>
            <li>nombre de bagages par passagers et possibilité ou non de les stocker,</li>
            <li>validité des permis des conducteurs et de l’assurance de la voiture utilisée.</li>
        </ul>
    </div>
    <h3>8. Quelles formalités dois-je effectuer auprès de mon assureur ?</h3>
    <div>
        <p>En tant que passager : Chacun dispose d'une assurance responsabilité civile qui est obligatoire et qui couvre les tiers et donc l'ensemble des partenaires du covoiturage, en cas d'accident survenu du fait de sa responsabilité.</p>
        <p>En tant que conducteur : Pour des raisons de sécurité, il est recommandé que le conducteur déclare le recours au covoiturage auprès de sa compagnie d'assurance afin que celle-ci lui communique les informations relatives aux conditions de mise en œuvre de ce covoiturage.</p>
        <p>Retrouvez toutes les précisions liées à l'assurance du covoiturage sur le site de la Fédération Française des Sociétés d’Assurances : <a href="www.ffsa.fr" target="_blank">www.ffsa.fr</a></p>
